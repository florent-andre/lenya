package org.apache.lenya.xpatch;

public class XpatchException extends Exception {
	
	private static final long serialVersionUID = 1L;

	public XpatchException() {
		super();
	}

	public XpatchException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public XpatchException(String arg0) {
		super(arg0);
	}

	public XpatchException(Throwable arg0) {
		super(arg0);
	}

	
}

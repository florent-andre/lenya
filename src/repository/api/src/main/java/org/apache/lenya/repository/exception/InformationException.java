package org.apache.lenya.repository.exception;

public class InformationException extends Exception {
	
	private static final long serialVersionUID = 1L;
	
	public InformationException() {
		super();
	}

	public InformationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InformationException(String arg0) {
		super(arg0);
	}

	public InformationException(Throwable arg0) {
		super(arg0);
	}

}
